#!/usr/bin/env bash

# This script regenerates all Protobuf/gRPC client files.
#
# For now, it must be run manually, and the results are checked into source control. Eventually we
# might choose to automate this process as part of the overall build so that it's less manual and
# hence error prone.
#
# This script requires mise-managed tools: protoc, protoc-gen-go, protoc-gen-go-grpc,
# grpc-tools, grpc_tools_node_protoc_ts, protoc-gen-js.
# Python tools are invoked via uv run --with.

set -o errexit
set -o pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
PROTO_DIR="$REPO_ROOT/proto"
cd "$PROTO_DIR"

PROTOC_DIR="$(mise where protoc)"
PROTOC_INCLUDE="$PROTOC_DIR/include"

PROTOC_VERSION=$(protoc --version | head -n1 | tr -d '\n\r')
printf "Generated by version %s of protoc\n" "$PROTOC_VERSION" > grpc_version.txt

echo "* Generating Protobuf/gRPC SDK files:"
echo -e "\tVERSION: $PROTOC_VERSION"

# `status.proto` is in our source tree so that we can implement initialization failure in the
# dynamic client (written in JS) using the protobuf notion of "details" -- arbitrary protobuf
# messages packaged up inside of an error. Hence, JavaScript and Python include it but Go does not.

GO_PULUMIRPC="$REPO_ROOT/sdk/proto/go"
PROTO_FILES_NO_STATUS=$(find . -name "*.proto" -not -name "status.proto")
TEMP_DIR_GO=/tmp/go-build

echo -e "\tGO temp dir: $TEMP_DIR_GO"
mkdir -p "$TEMP_DIR_GO"

# shellcheck disable=SC2086
protoc \
    -I. -I"$PROTOC_INCLUDE" \
    --go_out="$TEMP_DIR_GO" --go_opt=paths=source_relative \
    --go-grpc_out="$TEMP_DIR_GO" --go-grpc_opt=paths=source_relative \
    $PROTO_FILES_NO_STATUS

rm -rf "${GO_PULUMIRPC:?}"/*
cp -r "$TEMP_DIR_GO"/pulumi/* "$GO_PULUMIRPC"

# Protoc for JavaScript has a bug where it emits Google Closure Compiler directives in the module
# prologue that mutate the global object, which causes side-by-side bugs in pulumi/pulumi
# (pulumi/pulumi#2401). The protoc compiler absolutely should not be emitting commonjs modules that
# mutate global, but alas, it does, and we have to sed the output to not do that.
#
# We're replacing the literal code string
#   var global = Function('return this')();
# with
#   var proto = { pulumirpc: ... }, global = proto;
#
# This sets up the remainder of the protobuf file so that it works fine, but doesn't mess with
# global. Note that we have to skip this transformation for the google/protobuf/status_pb.js file
# because it _does_ depend on that global state
# (https://github.com/pulumi/pulumi/pull/2403#issuecomment-458673703).

JS_PULUMIRPC="$REPO_ROOT/sdk/nodejs/proto"
JS_PROTOFLAGS="import_style=commonjs,binary"
PROTO_FILES=$(find . -name "*.proto")
TEMP_DIR_NODE=/tmp/nodejs-build

echo -e "\tJavaScript: $JS_PULUMIRPC [$JS_PROTOFLAGS]"
echo -e "\tJavaScript temp dir: $TEMP_DIR_NODE"
mkdir -p "$TEMP_DIR_NODE"

GRPC_NODE_PLUGIN="$(command -v grpc_tools_node_protoc_plugin)"
PROTOC_GEN_TS="$(command -v protoc-gen-ts)"

# shellcheck disable=SC2086
protoc \
    -I. -I"$PROTOC_INCLUDE" \
    --js_out="$JS_PROTOFLAGS:$TEMP_DIR_NODE" \
    --ts_out="grpc_js:$TEMP_DIR_NODE" \
    --grpc_out="grpc_js,minimum_node_version=6:$TEMP_DIR_NODE" \
    --plugin=protoc-gen-grpc="$GRPC_NODE_PLUGIN" \
    --plugin=protoc-gen-ts="$PROTOC_GEN_TS" \
    $PROTO_FILES

find "$TEMP_DIR_NODE/pulumi" -type f -name "*.ts" -exec perl -i -pe 's|\.\./pulumi/|./|g' {} \;
find "$TEMP_DIR_NODE/pulumi" -type f -name "*.ts" -exec perl -i -pe 's|/\./|/|g' {} \;
find "$TEMP_DIR_NODE/pulumi" -type f -name "*.js" -exec perl -i -0777 -pe \
    's/^var global\s*=.*?Function\(.*?this.*?\)\(\);/var proto = { pulumirpc: { codegen: { }, testing: { } } }, global = proto;/ms' {} \;
find "$TEMP_DIR_NODE/pulumi" -type f -name "*.js" -exec perl -i -pe "s|require\('../pulumi/|require('./|g" {} \;
find "$TEMP_DIR_NODE/pulumi" -type f -name "*.js" -exec perl -i -pe "s|require\('../../pulumi/|require('../|g" {} \;

rm -rf "${JS_PULUMIRPC:?}"/*
cp "$TEMP_DIR_NODE"/google/protobuf/*.js "$JS_PULUMIRPC"
cp "$TEMP_DIR_NODE"/google/protobuf/*.ts "$JS_PULUMIRPC"
cp -r "$TEMP_DIR_NODE"/pulumi/* "$JS_PULUMIRPC"

# Protoc for Python has a bug where, if your proto files are all in the same directory relative
# to one another, imports of said proto files will produce imports that don't work using Python 3.
#
# Since our proto files are all in the same directory, this little bit of sed rewrites the broken
# imports that protoc produces, of the form
#     import foo_pb2 as bar
# to the form
#     from . import foo_pb2 as bar
# This form is semantically equivalent and is accepted by both Python 2 and Python 3.
#
# mypy_protoc doesn't yet support aio which we use
# (https://github.com/nipunn1313/mypy-protobuf/issues/216) so we 'sed' to do some replacements to
# support that, adding an import for grpc.aio and typing and changing grpc.Server to
# Union[grpc.Server, grpc.aio.Server].
#
# mypy_protoc also marks all server methods as abstract; they aren't really abstract because they
# have default implementations that simply return a grpc error that it's not implemented. We make
# use of those default methods so don't want type checking telling us to fill them all in.

PY_PULUMIRPC="$REPO_ROOT/sdk/python/lib/pulumi/runtime/proto"
TEMP_DIR_PY=/tmp/python-build

echo -e "\tPython: $PY_PULUMIRPC"
echo -e "\tPython temp dir: $TEMP_DIR_PY"
mkdir -p "$TEMP_DIR_PY"

# shellcheck disable=SC2086
uv run --python 3.9 \
    --with "setuptools<71" \
    --with "grpcio==1.59.0" \
    --with "grpcio-tools==1.59.0" \
    --with "mypy-protobuf==3.6.0" \
    python3 -m grpc_tools.protoc \
    -I. -I"$PROTOC_INCLUDE" \
    --python_out="$TEMP_DIR_PY" \
    --mypy_out="$TEMP_DIR_PY" \
    --grpc_python_out="$TEMP_DIR_PY" \
    --mypy_grpc_out="$TEMP_DIR_PY" \
    $PROTO_FILES

perl -i -pe 's/^from pulumi import ([^ ]*)_pb2 as ([^ ]*)$/from . import $1_pb2 as $2/' "$TEMP_DIR_PY"/pulumi/*.py
perl -i -pe 's/^from pulumi\.codegen import ([^ ]*)_pb2 as ([^ ]*)$/from .codegen import $1_pb2 as $2/' "$TEMP_DIR_PY"/pulumi/*.py
perl -i -pe 's/^import grpc$/import grpc\nimport grpc.aio\nimport typing/' "$TEMP_DIR_PY"/pulumi/*.pyi
perl -i -pe 's/: grpc\.Server/: typing.Union[grpc.Server, grpc.aio.Server]/g' "$TEMP_DIR_PY"/pulumi/*.pyi
perl -i -pe 's/\@abc\.abstractmethod//' "$TEMP_DIR_PY"/pulumi/*.pyi

rm -rf "${PY_PULUMIRPC:?}"/*
cp -r "$TEMP_DIR_PY"/pulumi/* "$PY_PULUMIRPC"
cp -r "$TEMP_DIR_PY"/google/protobuf/* "$PY_PULUMIRPC"

# python protoc doesn't generate __init__.py files, and autogenerating them is a bit tricky, so we
# just restore them from git after each generate.
cd "$REPO_ROOT"
git diff --name-only --diff-filter D | grep __init__.py | xargs git restore

echo "* Done."
