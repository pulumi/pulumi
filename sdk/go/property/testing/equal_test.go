package test

import (
	"fmt"
	"maps"
	"slices"
	"strings"
	"testing"

	"github.com/stretchr/testify/assert"
	"pgregory.net/rapid"

	"github.com/pulumi/pulumi/sdk/v3/go/property"
)

func TestSameIsEqual(t *testing.T) {
	t.Parallel()
	rapid.Check(t, func(t *rapid.T) {
		same := Value(10).Draw(t, "value")
		assert.True(t, same.Equals(same))
	})
}

func TestDifferentIsNotEqual(t *testing.T) {
	t.Parallel()
	rapid.Check(t, func(t *rapid.T) {
		arr := rapid.SliceOfNDistinct(Value(10), 2, 2, valueKey).Draw(t, "values")
		v1, v2 := arr[0], arr[1]
		assert.False(t, v1.Equals(v2))
	})
}

// valueKey returns a string that distinguishes values exactly as [property.Value.Equals]
// does. %#v is not usable as the key: it prints pointer addresses for assets and
// archives nested inside an archive's Assets map, making structurally identical draws
// look distinct. Asset and archive equality is hash-based, and hashes generated by this
// package are content-derived, so the hash stands in for the full structure.
func valueKey(v property.Value) string {
	var b strings.Builder
	writeValueKey(&b, v)
	return b.String()
}

func writeValueKey(b *strings.Builder, v property.Value) {
	if v.Secret() {
		b.WriteString("secret:")
	}
	if deps := v.Dependencies(); len(deps) > 0 {
		fmt.Fprintf(b, "deps%q:", deps)
	}
	switch {
	case v.IsAsset():
		fmt.Fprintf(b, "asset:%s", v.AsAsset().Hash)
	case v.IsArchive():
		fmt.Fprintf(b, "archive:%s", v.AsArchive().Hash)
	case v.IsArray():
		b.WriteString("[")
		for _, e := range v.AsArray().AsSlice() {
			writeValueKey(b, e)
			b.WriteString(",")
		}
		b.WriteString("]")
	case v.IsMap():
		m := v.AsMap().AsMap()
		b.WriteString("{")
		for _, k := range slices.Sorted(maps.Keys(m)) {
			fmt.Fprintf(b, "%q:", k)
			writeValueKey(b, m[k])
			b.WriteString(",")
		}
		b.WriteString("}")
	default:
		fmt.Fprintf(b, "%#v", v)
	}
}
