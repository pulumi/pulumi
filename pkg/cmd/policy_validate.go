package cmd

import (
	"encoding/json"

	"github.com/pulumi/pulumi/pkg/backend"
	"github.com/pulumi/pulumi/sdk/go/common/util/cmdutil"
	"github.com/spf13/cobra"
)

func newPolicyValidateCmd() *cobra.Command {
	var argConfig string

	var cmd = &cobra.Command{
		Use:   "validate <org-name>/<policy-pack-name> <version>",
		Args:  cmdutil.ExactArgs(2),
		Short: "[PREVIEW] Validate Policy Pack configuration against config schema",
		Long:  "Validate Policy Pack configuration against the specified version of its configuration schema.",
		Run: cmdutil.RunFunc(func(cmd *cobra.Command, cliArgs []string) error {
			// Obtain current PolicyPack, tied to the Pulumi service backend.
			policyPack, err := requirePolicyPack(cliArgs[0])
			if err != nil {
				return err
			}

			// Get version from cmd argument
			version := &cliArgs[1]

			// Load the configuration from the user-specified JSON file into config object.
			var config map[string]*json.RawMessage
			if argConfig != "" {
				config, err = loadPolicyConfigFromFile(argConfig)
				if err != nil {
					return err
				}
			}

			return policyPack.Validate(commandContext(),
				backend.PolicyPackOperation{
					VersionTag: version,
					Scopes:     cancellationScopes,
					Config:     config,
				})
		}),
	}

	cmd.Flags().StringVar(&argConfig, "config", "",
		"The file path for the Policy Pack configuration file")
	cmd.MarkFlagRequired("config") // nolint: errcheck

	return cmd
}
