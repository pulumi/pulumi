func pythonSafeIdent(name string) string {
	if name == "" {
		return "generated"
	}
	var b strings.Builder
	for i, r := range name {
		if (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || r == '_' || (i > 0 && r >= '0' && r <= '9') {
			b.WriteRune(r)
		} else {
			b.WriteByte('_')
		}
	}
	result := b.String()
	if result == "" {
		return "generated"
	}
	if result[0] >= '0' && result[0] <= '9' {
		return "_" + result
	}
	return result
}

func pythonQuote(v string) string {
	b, _ := json.Marshal(v)
	return string(b)
}

func workflowPrimitivePythonType(typ string) string {
	switch strings.TrimSpace(strings.ToLower(typ)) {
	case "bool", "boolean":
		return "bool"
	case "int", "integer":
		return "int"
	case "number", "float":
		return "float"
	case "string":
		return "str"
	default:
		return "Any"
	}
}

func workflowTokenMember(token string) string {
	token = strings.TrimSpace(token)
	if token == "" {
		return ""
	}
	if idx := strings.LastIndex(token, ":"); idx != -1 && idx < len(token)-1 {
		return token[idx+1:]
	}
	return token
}

func workflowTypeRefToPythonType(
	ref *codegenrpc.TypeReference,
	fallbackName string,
	declared map[string]bool,
	b *strings.Builder,
) string {
	if ref == nil {
		return "Any"
	}

	if ref.GetObject() != nil && len(ref.GetObject().GetProperties()) > 0 {
		typeName := pythonSafeIdent(fallbackName)
		if !declared[typeName] {
			declared[typeName] = true
			b.WriteString("@dataclass\n")
			b.WriteString("class " + typeName + ":\n")
			fieldNames := make([]string, 0, len(ref.GetObject().GetProperties()))
			for name := range ref.GetObject().GetProperties() {
				fieldNames = append(fieldNames, name)
			}
			sort.Strings(fieldNames)
			for _, fieldName := range fieldNames {
				field := ref.GetObject().GetProperties()[fieldName]
				b.WriteString("    " + pythonSafeIdent(fieldName) + ": " + workflowPrimitivePythonType(field.GetType()) + "\n")
			}
			b.WriteString("\n")
		}
		return typeName
	}

	token := ref.GetToken()
	if token == "" {
		return "Any"
	}
	if !strings.Contains(token, ":") {
		return workflowPrimitivePythonType(token)
	}
	return "Any"
}

func GenerateWorkflowPackage(
	ctx context.Context,
	directory string,
	descriptor *codegenrpc.WorkflowPackageDescriptor,
	loader codegenrpc.WorkflowLoaderClient,
) error {
	if descriptor == nil {
		return errors.New("workflow package descriptor is required")
	}

	if err := os.MkdirAll(directory, 0o700); err != nil {
		return fmt.Errorf("create output directory: %w", err)
	}

	reqPkg := &codegenrpc.GetWorkflowPackageInfoRequest{Package: descriptor}
	pkgInfoResp, err := loader.GetPackageInfo(ctx, reqPkg)
	if err != nil {
		return err
	}
	pkgName := descriptor.GetName()
	pkgVersion := descriptor.GetVersion()
	if pkgVersion == "" {
		pkgVersion = "0.0.0"
	}
	if info := pkgInfoResp.GetPackage(); info != nil {
		if info.GetName() != "" {
			pkgName = info.GetName()
		}
		if info.GetVersion() != "" {
			pkgVersion = info.GetVersion()
		}
	}

	graphsResp, err := loader.GetGraphs(ctx, &codegenrpc.GetWorkflowGraphsRequest{Package: descriptor})
	if err != nil {
		return err
	}
	triggersResp, err := loader.GetTriggers(ctx, &codegenrpc.GetWorkflowTriggersRequest{Package: descriptor})
	if err != nil {
		return err
	}
	jobsResp, err := loader.GetJobs(ctx, &codegenrpc.GetWorkflowJobsRequest{Package: descriptor})
	if err != nil {
		return err
	}
	stepsResp, err := loader.GetSteps(ctx, &codegenrpc.GetWorkflowStepsRequest{Package: descriptor})
	if err != nil {
		return err
	}

	moduleName := PyPack("", pkgName)
	moduleDir := filepath.Join(directory, moduleName)
	if err := os.MkdirAll(moduleDir, 0o700); err != nil {
		return fmt.Errorf("create workflow sdk module directory: %w", err)
	}

	var b strings.Builder
	b.WriteString("# Code generated by pulumi-language-python for workflow SDK generation. DO NOT EDIT.\n")
	b.WriteString("from dataclasses import dataclass\n")
	b.WriteString("from typing import Any, Callable, Optional\n")
	b.WriteString("from pulumi import Output\n")
	b.WriteString("import pulumi.workflow as workflow\n\n")
	b.WriteString("PACKAGE_NAME = " + pythonQuote(pkgName) + "\n\n")

	declaredTypes := map[string]bool{}

	if len(graphsResp.GetGraphs()) == 0 && len(triggersResp.GetTriggers()) == 0 &&
		len(jobsResp.GetJobs()) == 0 && len(stepsResp.GetSteps()) == 0 {
		b.WriteString("def _noop() -> None:\n    pass\n")
	} else {
		for _, graph := range graphsResp.GetGraphs() {
			token := graph.GetToken()
			name := pythonSafeIdent(token)
			b.WriteString(
				"def graph_" + name +
					"(registry: workflow.WorkflowRegistry, fn: Callable[[workflow.Context], None]) -> None:\n",
			)
			b.WriteString("    registry.graph(" + pythonQuote(token) + ")(fn)\n\n")
		}
		for _, trigger := range triggersResp.GetTriggers() {
			name := pythonSafeIdent(trigger)
			b.WriteString(
				"def trigger_" + name +
					"(ctx: workflow.Context, name: str, spec: Optional[Any] = None, " +
					"options: Optional[workflow.TriggerOptions] = None):\n",
			)
			b.WriteString("    return ctx.trigger(name, " + pythonQuote(trigger) + ", spec, options=options)\n\n")
		}
		for _, job := range jobsResp.GetJobs() {
			token := job.GetToken()
			name := pythonSafeIdent(token)
			b.WriteString("def job_" + name + "(ctx: workflow.Context, options: Optional[workflow.JobOptions] = None):\n")
			b.WriteString("    if options is None:\n")
			b.WriteString("        return ctx.job(" + pythonQuote(token) + ")\n")
			b.WriteString("    return ctx.job(" + pythonQuote(token) + ", options)\n\n")
		}
		for _, stepToken := range stepsResp.GetSteps() {
			stepResp, err := loader.GetStep(ctx, &codegenrpc.GetWorkflowStepRequest{
				Package: descriptor,
				Token:   stepToken,
			})
			if err != nil {
				return err
			}

			memberName := workflowTokenMember(stepToken)
			name := pythonSafeIdent(memberName)
			inputType := workflowTypeRefToPythonType(
				stepResp.GetInputType(),
				"step_"+name+"_args",
				declaredTypes,
				&b,
			)
			outputType := workflowTypeRefToPythonType(
				stepResp.GetOutputType(),
				"step_"+name+"_result",
				declaredTypes,
				&b,
			)

			b.WriteString(
				"def step_" + name +
					"(job: workflow.JobContext, args: " + inputType +
					", options: Optional[workflow.StepOptions] = None) -> Output[" + outputType + "]:\n",
			)
			b.WriteString("    if options is None:\n")
			b.WriteString("        return job.step(" + pythonQuote(stepToken) + ", args)\n")
			b.WriteString("    return job.step(" + pythonQuote(stepToken) + ", args, options)\n\n")
		}
	}

	if err := os.WriteFile(filepath.Join(moduleDir, "__init__.py"), []byte(b.String()), 0o600); err != nil {
		return fmt.Errorf("write workflow sdk module: %w", err)
	}

	if err := os.WriteFile(filepath.Join(moduleDir, "py.typed"), []byte(""), 0o600); err != nil {
		return fmt.Errorf("write workflow sdk py.typed: %w", err)
	}

	setupPy := strings.Join([]string{
		"from setuptools import setup",
		"",
		"setup(",
		"    name=" + pythonQuote(moduleName) + ",",
		"    version=" + pythonQuote(pkgVersion) + ",",
		"    packages=[" + pythonQuote(moduleName) + "],",
		"    package_data={" + pythonQuote(moduleName) + ": [\"py.typed\"]},",
		")",
		"",
	}, "\n")
	if err := os.WriteFile(filepath.Join(directory, "setup.py"), []byte(setupPy), 0o600); err != nil {
		return fmt.Errorf("write workflow sdk setup.py: %w", err)
	}

	return nil
}
