// Command versiongen regenerates pkg/util/known_runtimes_gen.go from the repo-root
// versions.json, the single source of truth for pinned language-runtime versions. It is run
// by `make generate-versions`.
package main

import (
	"bytes"
	"encoding/json"
	"fmt"
	"go/format"
	"os"
	"sort"
	"strings"
	"text/template"
)

// languageRuntime mirrors one entry of versions.json: the GitHub owner/repo hosting the
// runtime's releases and the pinned release tag.
type languageRuntime struct {
	Repo    string `json:"repo"`
	Version string `json:"version"`
}

// versionsDocument captures only the part of versions.json that the CLI compiles in: the
// unbundled language runtimes it fetches on demand.
type versionsDocument struct {
	UnbundledLanguageRuntimes map[string]languageRuntime `json:"unbundledLanguageRuntimes"`
}

type templateEntry struct {
	Name    string
	Repo    string
	Version string
}

const licenseHeader = `// Copyright 2026, Pulumi Corporation.
//
// Licensed under the Apache License, Version 2.0 (the "License");
// you may not use this file except in compliance with the License.
// You may obtain a copy of the License at
//
//     http://www.apache.org/licenses/LICENSE-2.0
//
// Unless required by applicable law or agreed to in writing, software
// distributed under the License is distributed on an "AS IS" BASIS,
// WITHOUT WARRANTIES OR CONDITIONS OF ANY KIND, either express or implied.
// See the License for the specific language governing permissions and
// limitations under the License.`

const fileTemplate = `{{.License}}

// Code generated by "make generate-versions" from versions.json; DO NOT EDIT.

//nolint:revive // Legacy package name we don't want to change
package util

// unbundledLanguageRuntimeData mirrors the "unbundledLanguageRuntimes" section of the
// repo-root versions.json. It is consumed by knownLanguageRuntimes in plugin.go so the
// pinned, on-demand language runtimes the CLI fetches stay in lockstep with that single
// source of truth.
var unbundledLanguageRuntimeData = []unbundledLanguageRuntimeSpec{
{{- range .Entries}}
	{Name: {{printf "%q" .Name}}, Repo: {{printf "%q" .Repo}}, Version: {{printf "%q" .Version}}},
{{- end}}
}
`

func main() {
	if len(os.Args) != 3 {
		fmt.Fprintf(os.Stderr, "usage: %s <versions.json> <output.go>\n", os.Args[0])
		os.Exit(2)
	}
	if err := run(os.Args[1], os.Args[2]); err != nil {
		fmt.Fprintln(os.Stderr, "versiongen:", err)
		os.Exit(1)
	}
}

func run(inputPath, outputPath string) error {
	raw, err := os.ReadFile(inputPath)
	if err != nil {
		return fmt.Errorf("reading %s: %w", inputPath, err)
	}

	var doc versionsDocument
	if err := json.Unmarshal(raw, &doc); err != nil {
		return fmt.Errorf("parsing %s: %w", inputPath, err)
	}

	names := make([]string, 0, len(doc.UnbundledLanguageRuntimes))
	for name := range doc.UnbundledLanguageRuntimes {
		names = append(names, name)
	}
	sort.Strings(names)

	entries := make([]templateEntry, 0, len(names))
	for _, name := range names {
		rt := doc.UnbundledLanguageRuntimes[name]
		// plugin.go feeds Version straight to semver.MustParse, which rejects a leading "v".
		entries = append(entries, templateEntry{
			Name:    name,
			Repo:    rt.Repo,
			Version: strings.TrimPrefix(rt.Version, "v"),
		})
	}

	tmpl := template.Must(template.New("file").Parse(fileTemplate))
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, struct {
		License string
		Entries []templateEntry
	}{License: licenseHeader, Entries: entries}); err != nil {
		return fmt.Errorf("rendering template: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("formatting generated source: %w", err)
	}

	if err := os.WriteFile(outputPath, formatted, 0o600); err != nil {
		return fmt.Errorf("writing %s: %w", outputPath, err)
	}
	return nil
}
