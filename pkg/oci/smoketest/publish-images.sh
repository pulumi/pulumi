#!/usr/bin/env bash
#
# publish-images.sh — EMIT (don't run) the plan to publish the pulumi-pod image: ONE
# multi-arch artifact that bakes this branch's engine (pulumi CLI), the OCI language host
# (pulumi-language-oci), AND the pull-through provider registry proxy (registry-proxy).
#
#   <base>/pulumi-pod:<version>
#
# The engine is the default entrypoint; the wrapper runs the proxy from the SAME image via
# `--entrypoint registry-proxy`, so there is only one image to build, publish, and pull.
# (The bundled proxy is a trial-kit bootstrap convenience — a temporary measure that a real
# registry replaces later; bundling it just makes trying the kit a single-image affair.)
#
# This SCRIPT PRINTS the commands rather than running them, so the outward step (the
# `docker buildx --push`) never happens until you look at the plan and opt in:
#
#     ./publish-images.sh ghcr.io/pulumi-labs           # inspect the plan (tag defaults to latest)
#     ./publish-images.sh ghcr.io/pulumi-labs | sh      # run it
#
# Prereqs for running the emitted plan: the repo Go toolchain (cross-compiles on the host —
# no emulation), a Docker daemon with buildx, and `docker login <registry>` (for ghcr.io: a
# GitHub PAT with write:packages). The image is built FROM docker:cli (a multi-arch base)
# with pure COPYs, so no QEMU/binfmt is needed for the foreign architecture.
#
# Usage: publish-images.sh <registry-base> [version]
#   <registry-base>  e.g. ghcr.io/pulumi-labs  (owner/namespace, no image name)
#   [version]        image tag; default "latest"
set -euo pipefail

REGISTRY_BASE="${1:-}"
VERSION="${2:-latest}"
if [ -z "$REGISTRY_BASE" ]; then
  echo "usage: $(basename "$0") <registry-base> [version]   e.g. $(basename "$0") ghcr.io/pulumi-labs latest" >&2
  echo "       (prints the publish plan to stdout; pipe to sh to run it)" >&2
  exit 2
fi

SMOKE_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PKG_DIR="$(cd "$SMOKE_DIR/../.." && pwd)"                 # the pkg/ Go module (CLI + host)
PROXY_DIR="$(cd "$SMOKE_DIR/registry-proxy" && pwd)"     # the proxy's own Go module

POD_REF="$REGISTRY_BASE/pulumi-pod:$VERSION"

# Emit a self-contained plan. Values are baked into variables at the top; the body is flat
# so it reads top-to-bottom. Runtime-only refs ($WORK, and the vars just set) are escaped
# so they survive into the emitted script.
cat <<EOF
#!/bin/sh
# Publish plan for the pulumi-pod image — generated by publish-images.sh. Inspect, then
# run with \`sh\`. The only outward action is the \`docker buildx --push\` step.
set -e

POD_REF="$POD_REF"
PKG_DIR="$PKG_DIR"
PROXY_DIR="$PROXY_DIR"
POD_DOCKERFILE="$SMOKE_DIR/Dockerfile.pod.publish"
WORK="\$(mktemp -d)"

echo "==> cross-compiling engine binaries (linux/amd64, linux/arm64)"
( cd "\$PKG_DIR" && GOWORK=off GOOS=linux GOARCH=amd64 CGO_ENABLED=0 go build -o "\$WORK/pulumi-cli-linux-amd64" ./cmd/pulumi )
( cd "\$PKG_DIR" && GOWORK=off GOOS=linux GOARCH=arm64 CGO_ENABLED=0 go build -o "\$WORK/pulumi-cli-linux-arm64" ./cmd/pulumi )
( cd "\$PKG_DIR" && GOWORK=off GOOS=linux GOARCH=amd64 CGO_ENABLED=0 go build -o "\$WORK/pulumi-language-oci-linux-amd64" ./cmd/pulumi-language-oci )
( cd "\$PKG_DIR" && GOWORK=off GOOS=linux GOARCH=arm64 CGO_ENABLED=0 go build -o "\$WORK/pulumi-language-oci-linux-arm64" ./cmd/pulumi-language-oci )

echo "==> cross-compiling registry-proxy (linux/amd64, linux/arm64)"
( cd "\$PROXY_DIR" && GOWORK=off GOOS=linux GOARCH=amd64 CGO_ENABLED=0 go build -o "\$WORK/registry-proxy-linux-amd64" . )
( cd "\$PROXY_DIR" && GOWORK=off GOOS=linux GOARCH=arm64 CGO_ENABLED=0 go build -o "\$WORK/registry-proxy-linux-arm64" . )

echo "==> ensuring a multi-arch buildx builder (docker-container driver)"
docker buildx inspect pulumi-pod-publish >/dev/null 2>&1 || docker buildx create --name pulumi-pod-publish --driver docker-container --bootstrap

echo "==> building + pushing \$POD_REF (linux/amd64, linux/arm64)"
docker buildx build --builder pulumi-pod-publish --platform linux/amd64,linux/arm64 --push -t "\$POD_REF" -f "\$POD_DOCKERFILE" "\$WORK"

rm -rf "\$WORK"
echo "==> published: \$POD_REF"
echo "==> the wrapper defaults to this ref; per-run override with PULUMI_POD_ENGINE_IMAGE=\$POD_REF"
echo "==> if using ghcr.io, make the package public OR have members 'docker login ghcr.io' to pull"
EOF
